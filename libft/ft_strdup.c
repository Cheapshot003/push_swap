#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*rtn;
	size_t	len;

	len = ft_strlen(s1) + 1;
	rtn = malloc(len);
	if (rtn == 0)
		return (0);
	return ((char *)(ft_memcpy(rtn, s1, len)));
}
