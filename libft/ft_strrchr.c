char	*ft_strrchr(char *str, int c)
{
	char	*res;
	char	c1;

	c1 = c;
	res = 0;
	while (*str)
	{
		if (*str == c1)
			res = str;
		str++;
	}
	if (*str == c1)
		return (str);
	return (res);
}
