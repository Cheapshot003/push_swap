#include <stddef.h>

void	*ft_memchr(void *str, int c, size_t n)
{
	char	c1;

	c1 = c;
	while (n != 0)
	{
		if (*(char *)str == c1)
		{
			return (str);
		}
		str++;
		n--;
	}
	return (0);
}
