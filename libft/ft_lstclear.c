#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*l;
	t_list	*l1;

	l = *lst;
	while (l != 0)
	{
		l1 = l->next;
		ft_lstdelone(l, del);
		l = l1;
	}
	*lst = 0;
}
